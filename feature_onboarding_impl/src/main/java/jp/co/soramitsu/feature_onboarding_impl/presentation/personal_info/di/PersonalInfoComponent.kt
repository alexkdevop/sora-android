import dagger.Subcomponent
import jp.co.soramitsu.core_di.holder.scope.ScreenScope
import jp.co.soramitsu.feature_onboarding_impl.presentation.OnboardingRouter
import jp.co.soramitsu.feature_onboarding_impl.presentation.personal_info.PersonalInfoFragment

@Subcomponent(
    modules = [
        PersonalInfoModule::class
    ]
)
@ScreenScope
interface PersonalInfoComponent {

    @Subcomponent.Builder
    interface Builder {

        @BindsInstance
        fun withFragment(fragment: Fragment): Builder

        @BindsInstance
        fun withRouter(router: OnboardingRouter): Builder

        fun build(): PersonalInfoComponent
    }

    fun inject(personalInfoFragment: PersonalInfoFragment)
}